package udistrital.avanzada.veterinaria.modelo;

/**
 * Enum que representa los diferentes tipos de alimentos que pueden consumir las mascotas exóticas.
 * Define las categorías nutricionales principales para la alimentación de los animales.
 * 
 * @author Sistema Veterinaria
 * @version 1.0
 */
public enum TipoAlimento {
    /**
     * Productos lácteos y derivados
     */
    LACTEOS,
    
    /**
     * Carnes y proteínas animales
     */
    CARNES,
    
    /**
     * Verduras y vegetales
     */
    VERDURAS,
    
    /**
     * Frutas y alimentos dulces
     */
    FRUTAS,
    
    /**
     * Forrajes y pastos
     */
    FORRAJES,
    
    /**
     * Cereales y granos
     */
    CEREALES,
    
    /**
     * Leguminosas y legumbres
     */
    LEGUMINOSAS,
    
    /**
     * Alimentación omnívora (variada)
     */
    OMNIVORO
}
