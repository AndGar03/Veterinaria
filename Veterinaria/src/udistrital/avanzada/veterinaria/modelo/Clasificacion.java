package udistrital.avanzada.veterinaria.modelo;

/**
 * Enum que representa las diferentes clasificaciones de mascotas exóticas.
 * Define los tipos de animales que pueden ser gestionados en el sistema.
 * 
 * @author Sistema Veterinaria
 * @version 1.0
 */
public enum Clasificacion {
    /**
     * Reptiles: animales de sangre fría con escamas
     */
    REPTIL,
    
    /**
     * Mamíferos: animales de sangre caliente con pelo
     */
    MAMIFERO,
    
    /**
     * Aves: animales con plumas y capacidad de vuelo
     */
    AVE,
    
    /**
     * Anfibios: animales que viven tanto en agua como en tierra
     */
    ANFIBIO,
    
    /**
     * Peces: animales acuáticos con branquias
     */
    PEZ,
    
    /**
     * Invertebrados: animales sin columna vertebral
     */
    INVERTEBRADO
}
